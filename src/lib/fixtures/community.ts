import type { CommunityMatch, DeckEntry, DeckSnapshot } from "@/lib/types";

function entries(...names: Array<[number, string]>): DeckEntry[] {
  return names.map(([qty, name]) => ({
    qty,
    name,
    cardId: name.toUpperCase().replace(/[^A-Z0-9]+/g, "-"),
  }));
}

function snapshot(
  title: string,
  legend: string,
  sourceKey: string,
  extraMainDeck: Array<[number, string]>,
): DeckSnapshot {
  return {
    title,
    legend,
    legendKey: legend,
    sourceKey,
    sourceUrl: `https://piltoverarchive.com/decks/view/${sourceKey}`,
    legendEntry: { qty: 1, name: legend },
    runes: entries([1, "Momentum Sigil"], [1, "Echo Rune"]),
    battlefields: entries([1, "Sunken Archives"], [1, "Frost Veil"]),
    mainDeck: entries([3, "Arc Runner"], [3, "Skyfire Bolt"], ...extraMainDeck),
    sideboard: entries([2, "Spellbreaker"], [2, "Shield Array"]),
  };
}

